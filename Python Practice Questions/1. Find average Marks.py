'''
Question: Write a program to input marks of three tests of a student (all integers). Then calculate and print the average of all test marks.
Input format :
3 Test marks (in different lines)
Output format :
Average 
Sample Input 1 :
3 
4 
6
Sample Output 1 :
4.333333333333333
'''

